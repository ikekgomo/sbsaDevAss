

public class Tip {
	private int tipAmount;

	public Tip(){
		this.tipAmount = 0;
	}

	/**
	 * @return the tipAmount
	 */
	public int getTipAmount() {
		return tipAmount;
	}

	/**
	 * @param tipAmount the tipAmount to set
	 */
	public void setTipAmount(int tipAmount) {
		this.tipAmount = tipAmount;
	}
}
